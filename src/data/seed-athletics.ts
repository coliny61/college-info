export async function seedAthletics(
  prisma: any,
  schoolMap: Record<string, any>,
) {
  // ── 1. Create Sports ──────────────────────────────────────────────────
  const sportMap: Record<string, any> = {};

  for (const sport of seedSports) {
    const schoolId = schoolMap[sport.schoolId]?.id ?? sport.schoolId;

    const created = await prisma.sport.upsert({
      where: { id: sport.id },
      update: {
        schoolId,
        name: sport.name,
        gender: sport.gender,
        season: sport.season,
        division: sport.division,
        conference: sport.conference,
        headCoach: sport.headCoach,
        record: sport.record,
        ranking: sport.ranking,
        offensiveScheme: sport.offensiveScheme,
        defensiveScheme: sport.defensiveScheme,
        offenseDescription: sport.offenseDescription,
        defenseDescription: sport.defenseDescription,
        strengthProgram: sport.strengthProgram,
        nutritionProgram: sport.nutritionProgram,
        playStyle: sport.playStyle,
        programDescription: sport.programDescription,
        recentBowlGames: sport.recentBowlGames,
        conferenceStanding: sport.conferenceStanding,
      },
      create: {
        id: sport.id,
        schoolId,
        name: sport.name,
        gender: sport.gender,
        season: sport.season,
        division: sport.division,
        conference: sport.conference,
        headCoach: sport.headCoach,
        record: sport.record,
        ranking: sport.ranking,
        offensiveScheme: sport.offensiveScheme,
        defensiveScheme: sport.defensiveScheme,
        offenseDescription: sport.offenseDescription,
        defenseDescription: sport.defenseDescription,
        strengthProgram: sport.strengthProgram,
        nutritionProgram: sport.nutritionProgram,
        playStyle: sport.playStyle,
        programDescription: sport.programDescription,
        recentBowlGames: sport.recentBowlGames,
        conferenceStanding: sport.conferenceStanding,
      },
    });

    sportMap[sport.id] = created;
  }

  console.log(`  ✓ Created ${Object.keys(sportMap).length} sports`);

  // ── 2. Create Coaches ─────────────────────────────────────────────────
  // Delete existing coaches for these sports first to avoid duplicates
  const sportIds = Object.keys(sportMap);
  await prisma.coach.deleteMany({
    where: { sportId: { in: sportIds } },
  });

  let coachCount = 0;
  for (const coach of seedCoaches) {
    const sportId = sportMap[coach.sportId]?.id ?? coach.sportId;

    await prisma.coach.create({
      data: {
        sportId,
        name: coach.name,
        title: coach.title,
        imageUrl: coach.imageUrl,
        bio: coach.bio,
        yearsAtSchool: coach.yearsAtSchool,
        careerRecord: coach.careerRecord,
        championships: coach.championships,
        previousRoles: coach.previousRoles,
        awards: coach.awards,
        playersInNfl: coach.playersInNfl,
        videoUrl: coach.videoUrl,
        coachingTree: coach.coachingTree,
      },
    });
    coachCount++;
  }

  console.log(`  ✓ Created ${coachCount} coaches`);

  // ── 3. Create Facilities ──────────────────────────────────────────────
  // Delete existing facilities for these schools to avoid duplicates
  const schoolIds = [
    ...new Set(seedSports.map((s) => schoolMap[s.schoolId]?.id ?? s.schoolId)),
  ];
  await prisma.facility.deleteMany({
    where: { schoolId: { in: schoolIds } },
  });

  let facilityCount = 0;
  for (const facility of seedFacilities) {
    const schoolId = schoolMap[facility.schoolKey]?.id ?? facility.schoolKey;
    const sportId = facility.sportId
      ? sportMap[facility.sportId]?.id ?? facility.sportId
      : null;

    await prisma.facility.create({
      data: {
        schoolId,
        sportId,
        name: facility.name,
        type: facility.type,
        description: facility.description,
        imageUrl: null,
        panoramaUrl: null,
        panoramaType: null,
        videoUrl: null,
        sortOrder: facility.sortOrder,
        isRequired: false,
      },
    });
    facilityCount++;
  }

  console.log(`  ✓ Created ${facilityCount} facilities`);

  return sportMap;
}
