// -----------------------------------------------------------------------------
// Helper: generate timestamps spread across the last 30 days
// -----------------------------------------------------------------------------

function daysAgo(days: number, hoursOffset = 0): string {
  const d = new Date();
  d.setDate(d.getDate() - days);
  d.setHours(d.getHours() - hoursOffset);
  return d.toISOString();
}

